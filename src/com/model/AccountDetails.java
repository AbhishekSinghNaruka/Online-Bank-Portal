package com.model;

public class AccountDetails {
	private int id;
	private double balance;
	private int TransictionPassword;
	
	public int getId() {
		return id;
	}
	public void setID(int id) {
		this.id=id;
	}
	public double getBalance() {
		return balance;
	}
	public void setBalance(double balance) {
		this.balance = balance;
	}
	public int getTransictionPassword() {
		return TransictionPassword;
	}
	public void setTransictionPassword(int transictionPassword) {
		TransictionPassword = transictionPassword;
	}
	
	
	
}
